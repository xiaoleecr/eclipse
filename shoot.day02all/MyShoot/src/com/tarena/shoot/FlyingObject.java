package com.tarena.shoot;
import java.awt.image.BufferedImage;

/** 飞行物 */
public abstract class FlyingObject {
	protected BufferedImage image; //图片
	protected int width; //宽
	protected int height; //高
	protected int x;  //x坐标
	protected int y;  //y坐标

	/** 飞行物走一步 */
	public abstract void step();
	
	/** 敌人被子弹撞 this:敌人 bullet:子弹 */
	public boolean shootBy(Bullet bullet){
		int x1 = this.x;  //x1:敌人的x
		int x2 = this.x + this.width; //y1:敌人的x+敌人的宽
		int y1 = this.y;  //y1:敌人的y
		int y2 = this.y + this.height; //y2:敌人的y+敌人的高
		int bx = bullet.x; //bx:子弹的x
		int by = bullet.y; //by:子弹的y
		
		return bx>x1 && bx<x2
		       &&
		       by>y1 && by<y2; //bx在x1与x2之间，并且，by在y1与y2之间则表明撞上了
	}
	
}







